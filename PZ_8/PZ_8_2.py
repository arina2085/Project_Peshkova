#Сгенерировать словарь вида {0: 0, 1: 1, 2: 4, 3: 9, 4: 16, 5: 25, 6: 36}, удалить из него второй и третий элементы. Отобразить исходный и получившйся словарь. Использовать for, range.

d = {0: 0, 1: 1, 2: 4, 3: 9, 4: 16, 5: 25, 6: 36}
print("Исходный словарь:", d)

keys = list(d.keys())
new_d = {}
for i in range(len(keys)):
    if i != 1 and i != 2:
        key = keys[i]
        new_d[key] = d[key]

print("Получившийся словарь:", new_d)